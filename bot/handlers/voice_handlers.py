        # –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏
        user_state['recognized_text'] = edited_text
        user_state['waiting_for_edit'] = False
        
